      	VALUE_V						CHARACTER VARYING(2550) NOT NULL,	
      	LAST_UPDATED_TIME_DT		TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
      	
      	CONSTRAINT MS_ITEMS_PKEY PRIMARY KEY(ITEM_ID_N),
